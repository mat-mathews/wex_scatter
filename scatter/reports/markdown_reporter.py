def build_graph_markdown(
    graph: DependencyGraph,
    metrics: Dict[str, ProjectMetrics],
    ranked: List[Tuple[str, ProjectMetrics]],
    cycles: List[CycleGroup],
    clusters: Optional[List] = None,
    metadata: Optional[Dict] = None,
    dashboard: Optional[HealthDashboard] = None,
) -> str:
    """Build markdown string for dependency graph analysis report."""
    parts: List[str] = ["# Dependency Graph Analysis\n"]

    # Summary stats table
    parts.append(
        _md_table(
            ["Metric", "Value"],
            [
                ["Projects", str(graph.node_count)],
                ["Dependencies", str(graph.edge_count)],
                ["Connected components", str(len(graph.connected_components))],
                ["Circular dependencies", str(len(cycles))],
            ],
        )
    )
    parts.append("")

    # Top coupled projects
    if ranked:
        parts.append("## Top Coupled Projects\n")
        rows: List[List[str]] = []
        for name, m in ranked:
            rows.append(
                [
                    name,
                    f"{m.coupling_score:.1f}",
                    str(m.fan_in),
                    str(m.fan_out),
                    f"{m.instability:.2f}",
                ]
            )
        parts.append(
            _md_table(
                ["Project", "Score", "Fan-In", "Fan-Out", "Instability"],
                rows,
            )
        )
        parts.append("")

    # Cycles
    if cycles:
        parts.append("## Circular Dependencies\n")
        for i, cg in enumerate(cycles, 1):
            cycle_str = " \u2192 ".join(cg.shortest_cycle + [cg.shortest_cycle[0]])
            parts.append(f"{i}. **[{cg.size} projects]** {cycle_str}")
        parts.append("")

    # Domain clusters
    if clusters:
        parts.append("## Domain Clusters\n")
        rows = []
        for clu in clusters:
            label = f"{clu.extraction_feasibility} ({clu.feasibility_score:.3f})"
            rows.append(
                [
                    clu.name,
                    str(len(clu.projects)),
                    f"{clu.cohesion:.3f}",
                    f"{clu.coupling_to_outside:.3f}",
                    label,
                ]
            )
        parts.append(
            _md_table(
                ["Cluster", "Size", "Cohesion", "Coupling", "Feasibility"],
                rows,
            )
        )
        parts.append("")

    # Observations
    if dashboard and dashboard.observations:
        parts.append("## Observations\n")
        for obs in dashboard.observations:
            parts.append(f"- **[{obs.severity}]** {obs.message}")
        parts.append("")

    # Mermaid dependency diagram
    mermaid = generate_mermaid(graph, clusters=clusters, top_n=20)
    if mermaid and mermaid.strip() != "graph TD":
        parts.append("## Dependency Diagram\n")
        parts.append("```mermaid")
        parts.append(mermaid.rstrip())
        parts.append("```\n")

    meta = _fmt_metadata(metadata)
    if meta:
        parts.append(f"---\n{meta.lstrip()}")

    return "\n".join(parts)


def write_graph_markdown_report(
    graph: DependencyGraph,
    metrics: Dict[str, ProjectMetrics],
    ranked: List[Tuple[str, ProjectMetrics]],
    cycles: List[CycleGroup],
    output_file_path: Path,
    clusters: Optional[List] = None,
    metadata: Optional[Dict] = None,
    dashboard: Optional[HealthDashboard] = None,
) -> None:
    """Write dependency graph analysis report as markdown to file."""
    content = build_graph_markdown(
        graph,
        metrics,
        ranked,
        cycles,
        clusters=clusters,
        metadata=metadata,
        dashboard=dashboard,
    )
    _write_file(content, output_file_path)
