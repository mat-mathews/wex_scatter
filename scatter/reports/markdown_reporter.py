from scatter.analyzers.coupling_analyzer import CycleGroup, ProjectMetrics
from scatter.analyzers.health_analyzer import HealthDashboard
from scatter.core.graph import DependencyGraph
from scatter.core.models import FilterPipeline, ImpactReport
from scatter.reports.console_reporter import render_tree
from scatter.reports.graph_reporter import generate_mermaid


# ---------------------------------------------------------------------------
# Shared helpers
# ---------------------------------------------------------------------------


def _escape_cell(value: str) -> str:
    """Sanitize a value for use inside a markdown table cell."""
    return str(value).replace("|", "\\|").replace("\n", " ").strip()


def _fmt_metadata(metadata: Optional[Dict]) -> str:
    """Return an italic footer line with version and timestamp."""
    if metadata is None:
        return ""
    version = metadata.get("scatter_version", "unknown")
    timestamp = metadata.get("timestamp", "unknown")
    return f"\n*Generated by Scatter v{version} at {timestamp}*\n"


def _fmt_pipeline(pipeline: Optional[FilterPipeline]) -> str:
    """Return a markdown blockquote with search scope and filter chain."""
    if pipeline is None:
        return ""
    projects = f"{pipeline.total_projects_scanned:,}"
    files = f"{pipeline.total_files_scanned:,}"
    lines = [
        f"> **Search scope:** {pipeline.search_scope} (scanned {projects} projects, {files} files)"
    ]
    if pipeline.stages:
        lines.append(f"> **Filter:** {pipeline.format_arrow_chain()}")
    return "\n".join(lines) + "\n"


def _write_file(content: str, output_file_path: Path) -> None:
    """Write UTF-8 content to file, creating parent dirs as needed."""
    try:
        output_file_path.parent.mkdir(parents=True, exist_ok=True)
        with open(output_file_path, "w", encoding="utf-8") as f:
            f.write(content)
        logging.info(f"Successfully wrote markdown report to: {output_file_path}")
    except Exception as e:
        logging.error(f"Failed to write markdown report: {e}")


def _md_table(headers: List[str], rows: List[List[str]]) -> str:
    """Build a markdown table from headers and rows. All values escaped."""
    header_line = "| " + " | ".join(_escape_cell(h) for h in headers) + " |"
    separator = "| " + " | ".join("---" for _ in headers) + " |"
    lines = [header_line, separator]
    for row in rows:
        padded = list(row) + [""] * (len(headers) - len(row))
        lines.append("| " + " | ".join(_escape_cell(str(v)) for v in padded) + " |")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Legacy mode (git / target / sproc)
# ---------------------------------------------------------------------------


def build_markdown(
    detailed_results: List[Dict[str, Any]],
    metadata: Optional[Dict] = None,
    pipeline: Optional[FilterPipeline] = None,
    graph_metrics_requested: bool = False,
) -> str:
    """Build markdown string for legacy consumer analysis results."""
    parts: List[str] = ["# Consumer Analysis Report\n"]

    pipeline_block = _fmt_pipeline(pipeline)
    if pipeline_block:
        parts.append(pipeline_block)

    if not detailed_results:
        parts.append("No consuming relationships found.\n")
    else:
        # Group by (TargetProjectName, TriggeringType) — same as console reporter
        group_counts = Counter(
            (item["TargetProjectName"], item["TriggeringType"]) for item in detailed_results
        )
        last_target_type = None
        current_rows: List[List[str]] = []

        headers = ["Consumer", "Path", "Pipeline", "Solutions"]
        if graph_metrics_requested:
            headers.extend(["Coupling", "Fan-In", "Fan-Out", "Instability", "In Cycle"])

        def _flush_table() -> None:
            if current_rows:
                parts.append(_md_table(headers, current_rows))
                parts.append("")
                current_rows.clear()

        for item in detailed_results:
            current_target_type = (item["TargetProjectName"], item["TriggeringType"])
            if current_target_type != last_target_type:
                _flush_table()
                count = group_counts[current_target_type]
                parts.append(
                    f"## {item['TargetProjectName']} ({item['TargetProjectPath']}) ({count} consumer(s))\n"
                )
                if "N/A" not in item["TriggeringType"]:
                    parts.append(f"Type/Level: {item['TriggeringType']}\n")
                last_target_type = current_target_type

            pipeline_name = item.get("PipelineName") or "\u2014"
            solutions = item.get("ConsumingSolutions", [])
            solutions_str = ", ".join(solutions) if solutions else "\u2014"
            row: List[str] = [
                item["ConsumerProjectName"],
                item["ConsumerProjectPath"],
                pipeline_name,
                solutions_str,
            ]
            if graph_metrics_requested:
                cs = item.get("CouplingScore")
                row.extend(
                    [
                        str(cs) if cs is not None else "\u2014",
                        str(item.get("FanIn", "\u2014")),
                        str(item.get("FanOut", "\u2014")),
                        f"{item['Instability']:.3f}"
                        if item.get("Instability") is not None
                        else "\u2014",
                        "yes"
                        if item.get("InCycle")
                        else "no"
                        if item.get("InCycle") is not None
                        else "\u2014",
                    ]
                )
            current_rows.append(row)

        _flush_table()

        target_names = {item["TargetProjectName"] for item in detailed_results}
        parts.append(
            f"---\n**Total:** {len(detailed_results)} consuming relationship(s) across {len(target_names)} target(s).\n"
        )

    meta = _fmt_metadata(metadata)
    if meta:
        if not any(p.startswith("---") for p in parts[-2:]):
            parts.append("---")
        parts.append(meta.lstrip())

    return "\n".join(parts)


def write_markdown_report(
    detailed_results: List[Dict[str, Any]],
    output_file_path: Path,
    metadata: Optional[Dict] = None,
    pipeline: Optional[FilterPipeline] = None,
    graph_metrics_requested: bool = False,
) -> None:
    """Write legacy consumer analysis results as markdown to file."""
    content = build_markdown(
        detailed_results,
        metadata=metadata,
        pipeline=pipeline,
        graph_metrics_requested=graph_metrics_requested,
    )
    _write_file(content, output_file_path)


