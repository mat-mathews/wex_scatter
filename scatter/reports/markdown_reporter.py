def build_impact_markdown(
    report: ImpactReport,
    metadata: Optional[Dict] = None,
    graph_metrics_requested: bool = False,
) -> str:
    """Build markdown string for impact analysis report."""
    parts: List[str] = ["# Impact Analysis\n"]

    # Work request
    sow_display = report.sow_text[:200] + "..." if len(report.sow_text) > 200 else report.sow_text
    parts.append(f"**Work Request:** {sow_display}\n")

    # Risk / complexity summary
    risk_str = report.overall_risk or "Not assessed"
    complexity_str = report.complexity_rating or "Not assessed"
    effort_str = f" ({report.effort_estimate})" if report.effort_estimate else ""
    parts.append(f"**Overall Risk:** {risk_str} | **Complexity:** {complexity_str}{effort_str}\n")

    if not report.targets:
        parts.append("No analysis targets were identified.\n")
    else:
        for ti in report.targets:
            parts.append(f"## {ti.target.name}\n")
            parts.append(
                f"Direct Consumers: {ti.total_direct} | Transitive: {ti.total_transitive}\n"
            )

            if ti.consumers:
                # Blast radius tree in code block
                tree_lines = render_tree(ti.consumers)
                if tree_lines:
                    parts.append("### Blast Radius\n")
                    parts.append("```text")
                    parts.extend(tree_lines)
                    parts.append("```\n")

                # Flat consumer detail table
                parts.append("### Consumer Detail\n")
                detail_headers = [
                    "Consumer",
                    "Confidence",
                    "Depth",
                    "Via",
                    "Risk",
                    "Pipeline",
                    "Solutions",
                ]
                if graph_metrics_requested:
                    detail_headers.extend(
                        ["Coupling", "Fan-In", "Fan-Out", "Instability", "In Cycle"]
                    )
                rows: List[List[str]] = []
                for c in ti.consumers:
                    depth_display = "direct" if c.depth == 0 else str(c.depth)
                    via = c.propagation_parent or "\u2014"
                    risk = c.risk_rating or "\u2014"
                    pipeline_name = c.pipeline_name or "\u2014"
                    solutions = ", ".join(c.solutions) if c.solutions else "\u2014"
                    row = [
                        c.consumer_name,
                        c.confidence_label,
                        depth_display,
                        via,
                        risk,
                        pipeline_name,
                        solutions,
                    ]
                    if graph_metrics_requested:
                        row.extend(
                            [
                                str(c.coupling_score) if c.coupling_score is not None else "\u2014",
                                str(c.fan_in) if c.fan_in is not None else "\u2014",
                                str(c.fan_out) if c.fan_out is not None else "\u2014",
                                f"{c.instability:.3f}" if c.instability is not None else "\u2014",
                                "yes"
                                if c.in_cycle
                                else "no"
                                if c.in_cycle is not None
                                else "\u2014",
                            ]
                        )
                    rows.append(row)
                parts.append(_md_table(detail_headers, rows))
                parts.append("")

    # Complexity justification
    if report.complexity_justification:
        parts.append("### Complexity\n")
        parts.append(f"{report.complexity_rating}: {report.complexity_justification}\n")

    # Impact narrative
    if report.impact_narrative:
        parts.append("### Impact Summary\n")
        parts.append(f"{report.impact_narrative}\n")

    meta = _fmt_metadata(metadata)
    if meta:
        parts.append(f"---\n{meta.lstrip()}")

    return "\n".join(parts)


def write_impact_markdown_report(
    report: ImpactReport,
    output_file_path: Path,
    metadata: Optional[Dict] = None,
    graph_metrics_requested: bool = False,
) -> None:
    """Write impact analysis report as markdown to file."""
    content = build_impact_markdown(
        report, metadata=metadata, graph_metrics_requested=graph_metrics_requested
    )
    _write_file(content, output_file_path)


